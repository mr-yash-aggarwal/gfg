class Solution {
  public:
    vector<int> antiDiagonalPattern(vector<vector<int>> matrix) 
    {
        // Code here
        int n=matrix.size();
        vector<int>ans;
        for(int sum=0; sum<=2*n-1; sum++){
            int row=0;
            int col=sum;
            while(col>=n){
                row++;
                col--;
            }
            while(row<n && col>=0){
                ans.push_back(matrix[row][col]);
                row++;
                col--;
            }
        }
        return ans;
    }
};

